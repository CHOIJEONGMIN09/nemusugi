package ch3;

public class Person {
    private String name;
    private int age;

    // 기본 생성자
    public Person() {}

    // getter/setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
